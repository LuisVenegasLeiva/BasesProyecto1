    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            DefaultTableModel dm = (DefaultTableModel) tabla.getModel();
            int rowCount = dm.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
            }
            
            String query = "begin ? := datosMuestra(?); end;";

            CallableStatement stmt = conn.prepareCall(query);

            // register the type of the out param - an Oracle specific type
            stmt.registerOutParameter(1, OracleTypes.CURSOR);

            // set the in param
            Integer subasta = Integer.parseInt(this.jTextField1.getText());
            stmt.setInt(2, subasta);

            // execute and retrieve the result set
            stmt.execute();
            ResultSet rs = (ResultSet)stmt.getObject(1);

            // print the results
            DefaultTableModel mod = (DefaultTableModel) tabla.getModel();
            try{
                while (rs.next()) {
                    Object[] row = { rs.getString(1),rs.getString(2),rs.getInt(3)};
                    mod.addRow(row);
                    this.jButton1.setEnabled(true);
                    this.jComboBox1.setEnabled(true);
                    this.jTextArea1.setEnabled(true);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"El dato ingresado no es correcto");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

